from typing import List, Optional

import regex

TYPE_PROVINCE = 'province'
TYPE_CITY = 'city'
TYPE_DIRECT = 'direct'
TYPE_COUNTY = 'county'

P_NORMALIZE = regex.compile(r'(?:省|市|盟|地区|县)$')


class PlacePath:
    def __init__(self, place: 'Place') -> None:
        self.province: Optional['Place'] = None
        self.city: Optional['Place']  = None
        self.county: Optional['Place']  = None
        self.populate(place)

    def populate(self, place: 'Place'):
        while place is not None:
            if place.city_type == TYPE_COUNTY:
                self.county = place
            elif place.city_type in (TYPE_DIRECT, TYPE_CITY):
                self.city = place
            elif place.city_type == TYPE_PROVINCE:
                self.province = place
            place = place.parent

    def __str__(self) -> str:
        return f"({self.province}) >> ({self.city}) >> ({self.county})"

    def __repr__(self) -> str:
        return self.__str__()


class Place:
    __slots__ = ['id', 'name', 'shorts', 'parent', 'children_type', 'children', 'enabled']

    def __init__(self,
                 _id: str,
                 name: str,
                 shorts: Optional[List[str]] = None,
                 children_type: str = None,
                 children: Optional[List] = None,
                 enabled: bool = True) -> None:
        super().__init__()
        self.id: str = _id
        self.name: str = name
        self.shorts: Optional[List[str]] = shorts
        self.parent: Optional[Place] = None
        self.children_type = children_type
        self.children: Optional[List[Place]] = children
        if children is not None and len(children) > 0:
            for child in children:
                child.set_parent(self)
        self.enabled = enabled

    def set_parent(self, parent):
        self.parent = parent

    def set_children(self, children):
        if children is None or len(children) == 0:
            return
        self.children = children

    def add_child(self, child):
        if child is None:
            return
        if self.children is None:
            self.children = []
        self.children.append(child)
        child.set_parent(self)

    @property
    def city_type(self):
        return self.parent.children_type if self.parent else TYPE_PROVINCE

    def __str__(self) -> str:
        return P_NORMALIZE.sub('', self.name) if self.name else None

    def __repr__(self) -> str:
        if self.parent:
            return f"{self.parent} > {self.name}"
        else:
            return self.name

    def is_province(self) -> bool:
        return self.name[-1] == '省' or '自治区' in self.name or self.parent is None

    def prettify(self):
        def get_children_type(children_type):
            if children_type in ('CHILD_TYPE_CITY', 'city'):
                return 'CHILD_TYPE_CITY'
            if children_type in ('CHILD_TYPE_DIRECT', 'direct'):
                return 'CHILD_TYPE_DIRECT'
            if children_type in ('CHILD_TYPE_COUNTY', 'county'):
                return 'CHILD_TYPE_COUNTY'
            return children_type

        indent_factor = len(self.parent.id) // 2 if self.parent else 0
        indent = indent_factor * '\t'
        children = '\n'.join([c.prettify() for c in self.children]) if self.children and len(self.children) > 0 else None
        if children:
            return f"{indent}Place('{self.id}', '{self.name}', {get_children_type(self.children_type)}, [\n{children}\n{indent}]),"
        return f"{indent}Place('{self.id}', '{self.name}'),"


