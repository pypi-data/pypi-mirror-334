    def convert_editable_requirements( self, req_file_path: str ):
        """
        æŒ‡å®šã—ãŸ requirements.txt å†…ã® editable è¦ä»¶ (-e è¡Œ) ã®ç›¸å¯¾ãƒ‘ã‚¹ã‚’çµ¶å¯¾ãƒ‘ã‚¹ã«å¤‰æ›ã™ã‚‹ã€‚
        å¤‰æ›å¾Œã¯åŒã˜ãƒ•ã‚¡ã‚¤ãƒ«ã«ä¸Šæ›¸ãä¿å­˜ã™ã‚‹ã€‚

        :param req_file_path: requirements.txt ã®ãƒ•ã‚¡ã‚¤ãƒ«ãƒ‘ã‚¹ï¼ˆä¾‹: /content/drive/MyDrive/cEnv/app/kohya_ss/requirements.txtï¼‰
        """
        req_path = Path(req_file_path)
        base_dir = req_path.parent  # requirements.txt ãŒã‚ã‚‹ãƒ‡ã‚£ãƒ¬ã‚¯ãƒˆãƒª
        new_lines = []

        with req_path.open("r", encoding="utf-8") as f:
            for line in f:
                stripped = line.strip()
                if stripped.startswith("-e "):
                    # è¡Œã‚’åˆ†å‰²ã—ã¦ã€-e ã®å¾Œã‚ã®ãƒ‘ã‚¹éƒ¨åˆ†ã‚’å–å¾—
                    parts = stripped.split(maxsplit=1)
                    if len(parts) == 2:
                        rel_path = parts[1].split()[0]  # ã‚³ãƒ¡ãƒ³ãƒˆéƒ¨åˆ†ãªã©ã‚’é™¤å¤–ã™ã‚‹ãŸã‚æœ€åˆã®ãƒˆãƒ¼ã‚¯ãƒ³ã‚’å–å¾—
                        # ç›¸å¯¾ãƒ‘ã‚¹ã‚’çµ¶å¯¾ãƒ‘ã‚¹ã«å¤‰æ›
                        abs_path = os.path.abspath(os.path.join(base_dir, rel_path))
                        # ãã®å¾Œã‚ã«ã‚³ãƒ¡ãƒ³ãƒˆãŒã‚ã‚‹å ´åˆã¯ä¿æŒï¼ˆå…ƒã®è¡Œã®"-e"ä»¥é™ã®å…¨ä½“ã‚’ç½®æ›ã™ã‚‹ãªã‚‰ã‚³ãƒ¡ãƒ³ãƒˆã¯å‰Šé™¤ã•ã‚Œã‚‹ï¼‰
                        comment = ""
                        if "#" in parts[1]:
                            comment = " " + parts[1].split("#", 1)[1]
                        new_line = f"-e {abs_path}{comment}\n"
                        new_lines.append(new_line)
                    else:
                        new_lines.append(line)
                else:
                    new_lines.append(line)

        # æ–°ã—ã„å†…å®¹ã§ãƒ•ã‚¡ã‚¤ãƒ«ã‚’ä¸Šæ›¸ã
        with req_path.open("w", encoding="utf-8") as f:
            f.writelines(new_lines)
        self.debug.log_step(f"Editable è¦ä»¶ã®ãƒ‘ã‚¹ãŒçµ¶å¯¾ãƒ‘ã‚¹ã«å¤‰æ›ã•ã‚Œã¾ã—ãŸ: {req_file_path}", success = True )

